    # ── Init ──

    def _init_combat(self, combat_init: CombatInit) -> CombatState:
        """Set up combat state from CombatInit. Initiative by DEX descending.
        
        群组展开：quantity > 1 的 EnemyInstance 拆为独立战斗实体，分别计算 HP 和先攻。
        """
        import copy
        player = combat_init.player
        expanded_enemies = []
        for e in combat_init.enemies:
            qty = max(1, getattr(e, 'quantity', 1))
            per_hp = max(1, getattr(e, 'hp', 10) // qty)
            for i in range(qty):
                ce = copy.copy(e)
                ce.instance_id = f"{e.instance_id}_c{i}"
                ce.hp = per_hp
                ce.hp_max = per_hp
                ce.quantity = 1
                expanded_enemies.append(ce)
        state = CombatState(
            enemies=expanded_enemies,
            player_hp=player.derived.HP,
            player_hp_max=player.derived.HP,
            player_san=player.derived.SAN,
        )

        # Build initiative order: (actor_id, DEX)
        order = [("player", player.stats.DEX if hasattr(player, 'stats') else 50)]
        for enemy in expanded_enemies:
            dex = 50
            if hasattr(enemy, 'attributes'):
                dex = enemy.attributes.get("DEX", 50)
            elif hasattr(enemy, 'dex'):
                dex = enemy.dex
            order.append((enemy.instance_id, dex))
        order.sort(key=lambda x: -x[1])
        state.initiative_order = [oid for oid, _ in order]

        first_actor = state.initiative_order[0]
        state.is_player_turn = (first_actor == "player")
        for e in expanded_enemies:
            if getattr(e, 'boss_mechanics', ''):
                state._boss_hp_max = getattr(e, 'hp', state.player_hp)
                break
        for e in state.enemies:
            if not hasattr(e, 'hp_max') or not getattr(e, 'hp_max', 0):
                e.hp_max = getattr(e, 'hp', 10)
        return state

    def _match_action(self, raw_input: str, available: list[dict]) -> str:
        """Match raw player input to an available combat action id."""
        if not raw_input:
            return "punch"
        text = raw_input.lower()

        # Check each available action's label against player input
        for a in available:
            label = a["label"].lower()
            if label and label in text:
                return a["id"]

        # Keyword matching for fixed actions
        if any(kw in text for kw in ("拳", "打", "punch")):
            return "punch"
        if any(kw in text for kw in ("踢", "脚", "kick")):
            return "kick"
        if any(kw in text for kw in ("闪", "躲", "回避", "dodge")):
            return "dodge"
        if any(kw in text for kw in ("逃", "跑", "flee")):
            return "flee"
        if any(kw in text for kw in ("隐蔽", "潜行", "潜入", "conceal", "hide")):
            return "conceal"
        if any(kw in text for kw in ("瞄准", "aim")):
            return "aim"
        if any(kw in text for kw in ("蓄力", "charge")):
            return "charge"

        return "punch"

