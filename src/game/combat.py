# ── Module-level helpers ──

def _roll_damage(damage_spec, STR: int = 50, SIZ: int = 50) -> int:
    """Roll damage from structured spec or legacy string formula.

    新格式 (dict):
        {"dice_n": int, "dice_d": int, "bonus": int, "use_db": bool}
        伤害 = Σ(dice_n × D dice_d) + bonus + DB(if use_db)

    旧格式 (str):  "1D6+DB", "1D10+2", "4D6/2D6/1D6" 等，自动解析。
    """
    from investigator.rules import calc_db

    if damage_spec is None:
        return 0
    if not isinstance(damage_spec, (str, dict)):
        return 0

    # ── 旧格式字符串 → 自动转 dict ──
    if isinstance(damage_spec, str):
        damage_spec = _parse_legacy_damage(damage_spec)

    total = 0

    # base dice
    dice_n = damage_spec.get("dice_n", 0)
    dice_d = damage_spec.get("dice_d", 0)
    if dice_n > 0 and dice_d > 0:
        for _ in range(dice_n):
            total += random.randint(1, dice_d)

    # flat bonus
    total += damage_spec.get("bonus", 0)

    # damage bonus
    if damage_spec.get("use_db", False):
        db = calc_db(STR, SIZ)
        if db.startswith("+"):
            db = db[1:]
        if db.startswith("-"):
            total += int(db)
        elif "D" in db:
            total += _roll_damage(db, STR, SIZ)
        else:
            try:
                total += int(db)
            except ValueError:
                pass

    return max(0, total)


def _parse_legacy_damage(formula: str) -> dict:
    """Convert legacy damage formula string to structured dict. 一次迁移用。"""
    f = formula.replace(" ", "")
    spec = {"dice_n": 0, "dice_d": 0, "bonus": 0, "use_db": False}

    # 散弹枪射程格式
    if "/" in f:
        f = f.split("/")[0].strip()

    parts = f.split("+")
    for part in parts:
        part = part.strip()
        if part == "DB":
            spec["use_db"] = True
        elif "D" in part:
            m = re.match(r"(\d*)D(\d+)", part)
            if m:
                spec["dice_n"] = int(m.group(1)) if m.group(1) else 1
                spec["dice_d"] = int(m.group(2))
        else:
            try:
                spec["bonus"] += int(part)
            except ValueError:
                pass
    return spec


def _apply_armor(damage: int, armor: str) -> int:
    """Reduce damage by armor value. armor like '2点厚皮' -> 2."""
    m = re.search(r"(\d+)", armor)
    reduction = int(m.group(1)) if m else 0
    return max(0, damage - reduction)


def _apply_damage_multiplier(damage: int, damage_type: str, multipliers: dict) -> int:
    """Apply enemy damage multipliers: >1=vuln, <1=resist, 0=immune."""
    mult = multipliers.get(damage_type, 1.0)
    return max(0, int(damage * mult))


# ── Data structures ──

@dataclass
class CombatAction:
    """Record of one combat action (player or enemy)."""
    actor: str = ""           # "player" | enemy instance_id
    action_type: str = ""     # "attack" | "dodge" | "flee" | "special"
    weapon: str = ""
    skill_name: str = ""
    skill_value: int = 0
    roll: int = 0
    tier: str = ""            # fumble|failure|regular|hard|extreme
    target: str = ""
    damage: int = 0
    damage_type: str = "物理"
    hp_before: int = 0
    hp_after: int = 0
    narrative: str = ""
    success: bool = False
    round_num: int = 0


@dataclass
class CombatState:
    """Mutable combat state for one fight."""
    round: int = 1
    enemies: list[Any] = field(default_factory=list)
    player_hp: int = 0
    player_hp_max: int = 0
    player_san: int = 0
    initiative_order: list[str] = field(default_factory=list)
    is_player_turn: bool = True
    finished: bool = False
    log: list[CombatAction] = field(default_factory=list)
    full_log: list[CombatAction] = field(default_factory=list)
    _player_dodging: bool = False
    _player_concealed: bool = False
    _player_aim_counter: int = 0
    _boss_hp_max: int = 0
    _boss_current_phase: str = ""
    _correction_log: list[str] = field(default_factory=list)  # LLM per-round correction narratives


# ── Combat system ──

class CombatSystem:
    """COC 7th simplified combat controller. Independent of Keeper pipeline.

    Receives CombatInit, runs combat loop, returns CombatResult.
    Player actions: fixed options (punch/kick/weapon/dodge/flee).
    Enemy actions: weight-based attack selection, rule-driven targeting.

    llm_enhancement (bool, default from config): when True, per-round narrative
    is generated by LLM via build_combat_narrative_prompt() and the final
    CombatResult.narrative is populated. Currently disabled — plan B (O9).
    """

    def __init__(self, weapon_lib=None, llm_enhancement: bool = COMBAT_LLM_ENHANCEMENT):
        self.weapon_lib = weapon_lib
        self.llm_enhancement = llm_enhancement

    # ── Public API ──

    def run_combat(self, combat_init: CombatInit, player_action: str = "", max_rounds: int = 20) -> CombatResult:
        """Run full combat loop with layered execution: deterministic -> LLM gate -> apply."""
        state = self._init_combat(combat_init)
        player = combat_init.player
        environment_actions = getattr(combat_init, 'environment_actions', [])
        available = self._get_player_actions(player, environment_actions)

        action_id = player_action or combat_init.player_action or "punch"
        if not any(a["id"] == action_id for a in available):
            if action_id == "punch" and not any(a["id"] == "punch" for a in available):
                action_id = available[0]["id"] if available else "punch"

        targets = combat_init.player_targets or []
        player_extra = getattr(combat_init, 'player_extra', '') or ''

        for e in state.enemies:
            if not hasattr(e, 'hp_max') or not getattr(e, 'hp_max', 0):
                e.hp_max = getattr(e, 'hp', 10)
            if getattr(e, 'boss_mechanics', ''):
                state._boss_hp_max = getattr(e, 'hp_max', state.player_hp)

        round_log = []
        needs_llm = self._any_special_rules(combat_init, state.enemies)

        while not state.finished and state.round <= max_rounds:
            alive_enemies = [e for e in state.enemies
                            if getattr(e, 'hp', 1) > 0 and getattr(e, 'status', '') not in ('dead', 'defeated')]
            if not alive_enemies:
                state.finished = True
                break

            round_targets = targets if targets else [alive_enemies[0].instance_id]

            # ── 玩家多轮攻击：根据武器 multi_attack 扩展目标列表 ──
            match = next((a for a in available if a["id"] == action_id), None)
            multi = match.get("multi_attack", 1) if match else 1
            if multi > len(round_targets):
                last = round_targets[-1] if round_targets else alive_enemies[0].instance_id
                round_targets = round_targets + [last] * (multi - len(round_targets))

            player_actions_this_round = []
            enemy_actions_this_round = []
            state.log = []
            state._player_dodging = False

            for iid in state.initiative_order:
                if iid == "player":
                    for tgt in round_targets:
                        if not tgt:
                            tgt = alive_enemies[0].instance_id if alive_enemies else "unknown"
                        pa = self._resolve_player_action(
                            state, player, action_id, tgt, environment_actions
                        )
                        pa.round_num = state.round
                        state.log.append(pa)
                        state.full_log.append(pa)
                        player_actions_this_round.append({
                            "action_type": pa.action_type,
                            "target": pa.target,
                            "weapon": pa.weapon,
                            "roll": pa.roll,
                            "tier": pa.tier,
                            "damage": pa.damage,
                            "damage_type": getattr(pa, 'damage_type', '物理'),
                            "effects": [],
                        })
                        if state.finished:
                            break
                    if state.finished:
                        break
                    continue

                enemy = next((e for e in state.enemies if e.instance_id == iid), None)
                if not enemy or getattr(enemy, 'status', '') in ('dead', 'defeated') or getattr(enemy, 'hp', 1) <= 0:
                    continue

                multi = getattr(enemy, 'multi_attack', 1)
                for _ in range(multi):
                    ea = self._resolve_enemy_action(state, enemy, player)
                    ea.round_num = state.round
                    state.log.append(ea)
                    state.full_log.append(ea)
                    enemy_actions_this_round.append({
                        "actor": ea.actor,
                        "action_type": ea.action_type,
                        "roll": ea.roll,
                        "tier": ea.tier,
                        "damage": ea.damage,
                        "damage_type": getattr(ea, 'damage_type', '物理'),
                        "effects": [],
                    })
                    if state.player_hp <= 0:
                        state.finished = True
                        break
                if state.finished:
                    break

            rresult = self._build_round_result(
                state, player_actions_this_round, enemy_actions_this_round, state.round
            )

            if needs_llm:
                boss_phase = state._boss_current_phase or ""
                snapshot = self._build_battle_snapshot(state, player, boss_phase)
                rresult = self._llm_correct_round(
                    rresult, combat_init, state.enemies,
                    player_extra, snapshot, boss_phase, player_actions_this_round
                )
                corr_narr = rresult.get("narrative", "")
                if corr_narr:
                    state._correction_log.append(f"第{state.round}轮玩家修正: {corr_narr}")

                # ── 敌人 LLM 修正：每个有 special_rules 的敌人独立调用 ──
                inv_context = getattr(player, 'personal_description', '') or ''
                if getattr(player, 'extra', ''):
                    inv_context = (inv_context + '\n' + player.extra).strip()
                for ea_data in enemy_actions_this_round:
                    old_dmg = ea_data.get("damage", 0)
                    if old_dmg <= 0:
                        continue
                    enemy_id = ea_data.get("actor", "")
                    enemy = next((e for e in state.enemies
                                 if getattr(e, 'instance_id', '') == enemy_id), None)
                    if enemy and getattr(enemy, 'special_rules', ''):
                        corrected = self._llm_correct_enemy_round(
                            enemy, ea_data, player, player_extra, inv_context
                        )
                        new_dmg = max(0, corrected.get("damage", old_dmg))
                        state.player_hp = max(0, state.player_hp + old_dmg - new_dmg)
                        ea_data["damage"] = new_dmg
                        if corrected.get("narrative"):
                            ea_data["effects"] = ea_data.get("effects", []) + [corrected["narrative"]]
                            state._correction_log.append(f"第{state.round}轮{enemy_id}修正: {corrected['narrative']}")

            # 玩家伤害结算 — 每击独立应用
            for pa in player_actions_this_round:
                if pa.get("action_type") != "attack":
                    continue
                dmg = pa.get("damage", 0)
                tgt_iid = pa.get("target", "")
                enemy = next((e for e in state.enemies if getattr(e, 'instance_id', '') == tgt_iid), None)
                if not enemy:
                    continue
                # LLM 修正后的伤害优先，int() 防 None/str
                corrected_dmg = rresult.get("player_damage", 0)
                try:
                    effective = max(0, int(corrected_dmg if corrected_dmg is not None else dmg))
                except (ValueError, TypeError):
                    effective = max(0, int(dmg) if dmg else 0)
                enemy.hp = max(0, getattr(enemy, 'hp', 10) - effective)

            for enemy in state.enemies:
                if getattr(enemy, 'hp', 1) <= 0 or getattr(enemy, 'status', '') in ('dead', 'defeated'):
                    continue
                triggered = self._check_phase(state, enemy)
                if triggered:
                    desc = self._apply_phase(state, enemy, triggered, getattr(enemy, 'phases', []))
                    if desc:
                        rresult.setdefault("status_changes", []).append({
                            "entity_id": enemy.instance_id,
                            "field": "phase",
                            "old": "",
                            "new": triggered,
                        })

            round_log.append(rresult)

            alive_after = [e for e in state.enemies
                          if getattr(e, 'hp', 1) > 0 and getattr(e, 'status', '') != 'dead']
            if not alive_after:
                state.finished = True

            state.round += 1

        outcome = "win"
        if state.player_hp <= 0:
            outcome = "loss"
        elif state.round > max_rounds:
            outcome = "draw"

        combat_narrative = self._generate_combat_narrative(
            state, combat_init.player, combat_init.scene
        )

        return CombatResult(
            outcome=outcome,
            defeated_instance_ids=[],
            player_hp=state.player_hp,
            player_san=state.player_san,
            rounds=state.round,
            narrative=combat_narrative,
            round_log=round_log,
        )

    def run_single_round(self, combat_init: CombatInit, state: CombatState,
                         action_id: str, target_ids: list[str],
                         player_extra: str = "") -> dict:
        """Execute one combat round interactively. Returns result dict.

        Caller provides the current CombatState (with live HP/initiative/etc.).
        We execute one round and return the updated state metadata.
        """
        player = combat_init.player
        environment_actions = getattr(combat_init, 'environment_actions', [])
        available = self._get_player_actions(player, environment_actions)

        # Resolve action
        if not action_id or not any(a["id"] == action_id for a in available):
            action_id = "punch"

        alive_enemies = [e for e in state.enemies
                        if getattr(e, 'hp', 1) > 0 and getattr(e, 'status', '') not in ('dead', 'defeated')]
        if not alive_enemies:
            state.finished = True
            return self._build_single_round_result(state, combat_init)

        # Multi-attack target expansion
        match = next((a for a in available if a["id"] == action_id), None)
        multi = match.get("multi_attack", 1) if match else 1
        round_targets = list(target_ids) if target_ids else [alive_enemies[0].instance_id]
        if multi > len(round_targets):
            last = round_targets[-1] if round_targets else alive_enemies[0].instance_id
            round_targets = round_targets + [last] * (multi - len(round_targets))

        state.log = []
        state._player_dodging = False
        player_actions_this_round = []
        enemy_actions_this_round = []

        for iid in state.initiative_order:
            if iid == "player":
                for tgt in round_targets:
                    if not tgt:
                        tgt = alive_enemies[0].instance_id if alive_enemies else "unknown"
                    pa = self._resolve_player_action(state, player, action_id, tgt, environment_actions)
                    pa.round_num = state.round
                    state.log.append(pa)
                    state.full_log.append(pa)
                    player_actions_this_round.append({
                        "action_type": pa.action_type,
                        "target": pa.target,
                        "weapon": pa.weapon,
                        "roll": pa.roll,
                        "tier": pa.tier,
                        "damage": pa.damage,
                        "damage_type": getattr(pa, 'damage_type', '物理'),
                        "effects": [],
                    })
                    if state.finished:
                        break
                if state.finished:
                    break
                continue

            enemy = next((e for e in state.enemies if e.instance_id == iid), None)
            if not enemy or getattr(enemy, 'status', '') in ('dead', 'defeated') or getattr(enemy, 'hp', 1) <= 0:
                continue

            multi_e = getattr(enemy, 'multi_attack', 1)
            for _ in range(multi_e):
                ea = self._resolve_enemy_action(state, enemy, player)
                ea.round_num = state.round
                state.log.append(ea)
                state.full_log.append(ea)
                enemy_actions_this_round.append({
                    "actor": ea.actor,
                    "action_type": ea.action_type,
                    "roll": ea.roll,
                    "tier": ea.tier,
                    "damage": ea.damage,
                    "damage_type": getattr(ea, 'damage_type', '物理'),
                    "effects": [],
                })
                if state.player_hp <= 0:
                    state.finished = True
                    break
            if state.finished:
                break

        rresult = self._build_round_result(
            state, player_actions_this_round, enemy_actions_this_round, state.round
        )

        needs_llm = self._any_special_rules(combat_init, state.enemies)
        if needs_llm:
            boss_phase = state._boss_current_phase or ""
            snapshot = self._build_battle_snapshot(state, player, boss_phase)
            rresult = self._llm_correct_round(
                rresult, combat_init, state.enemies,
                player_extra, snapshot, boss_phase, player_actions_this_round
            )
            corr_narr = rresult.get("narrative", "")
            if corr_narr:
                state._correction_log.append(f"第{state.round}轮玩家修正: {corr_narr}")

            inv_context = getattr(player, 'personal_description', '') or ''
            if getattr(player, 'extra', ''):
                inv_context = (inv_context + '\n' + player.extra).strip()
            for ea_data in enemy_actions_this_round:
                old_dmg = ea_data.get("damage", 0)
                if old_dmg <= 0:
                    continue
                enemy_id = ea_data.get("actor", "")
                enemy = next((e for e in state.enemies
                             if getattr(e, 'instance_id', '') == enemy_id), None)
                if enemy and getattr(enemy, 'special_rules', ''):
                    corrected = self._llm_correct_enemy_round(
                        enemy, ea_data, player, player_extra, inv_context)
                    new_dmg = max(0, corrected.get("damage", old_dmg))
                    state.player_hp = max(0, state.player_hp + old_dmg - new_dmg)
                    ea_data["damage"] = new_dmg
                    if corrected.get("narrative"):
                        ea_data["effects"] = ea_data.get("effects", []) + [corrected["narrative"]]
                        state._correction_log.append(f"第{state.round}轮{enemy_id}修正: {corrected['narrative']}")

        # Apply player damage to enemies
        for pa in player_actions_this_round:
            if pa.get("action_type") != "attack":
                continue
            dmg = pa.get("damage", 0)
            tgt_iid = pa.get("target", "")
            enemy = next((e for e in state.enemies if getattr(e, 'instance_id', '') == tgt_iid), None)
            if not enemy:
                continue
            corrected_dmg = rresult.get("player_damage", 0)
            try:
                effective = max(0, int(corrected_dmg if corrected_dmg is not None else dmg))
            except (ValueError, TypeError):
                effective = max(0, int(dmg) if dmg else 0)
            enemy.hp = max(0, getattr(enemy, 'hp', 10) - effective)

        # Phase check
        for enemy in state.enemies:
            if getattr(enemy, 'hp', 1) <= 0 or getattr(enemy, 'status', '') == 'dead':
                continue
            triggered = self._check_phase(state, enemy)
            if triggered:
                desc = self._apply_phase(state, enemy, triggered, getattr(enemy, 'phases', []))
                if desc:
                    rresult.setdefault("status_changes", []).append({
                        "entity_id": enemy.instance_id,
                        "field": "phase",
                        "old": "",
                        "new": triggered,
                    })

        # Check end conditions
        alive_after = [e for e in state.enemies
                      if getattr(e, 'hp', 1) > 0 and getattr(e, 'status', '') != 'dead']
        if not alive_after:
            state.finished = True
        if state.player_hp <= 0:
            state.finished = True

        state.round += 1
        return self._build_single_round_result(state, combat_init)

    def _build_single_round_result(self, state: CombatState, combat_init) -> dict:
        """Build result dict after a single round."""
        is_boss = any(getattr(e, 'boss_mechanics', '') for e in state.enemies)
        outcome = None
        game_over = False

        if state.finished:
            player_fled = any(
                a.actor == "player" and a.action_type == "flee" and a.success
                for a in state.full_log
            )
            if player_fled:
                outcome = "flee"
            elif state.player_hp <= 0:
                outcome = "loss"
                game_over = not is_boss
            elif state.round > 20:
                outcome = "draw"
            else:
                outcome = "win"

        # Build concise round narrative
        lines = []
        for a in state.log:
            actor = "调查员" if a.actor == "player" else a.actor
            if a.action_type == "attack":
                s = "命中" if a.success else "未命中"
                dmg = f" 造成{a.damage}点伤害" if a.success and a.damage > 0 else ""
                lines.append(f"{actor} | {a.weapon} D100={a.roll} {s}{dmg}")
            elif a.action_type == "flee":
                lines.append(f"{actor} | 逃跑 {'成功' if a.success else '失败'}")
            elif a.action_type == "dodge":
                lines.append(f"{actor} | 回避")
            elif a.action_type in ("conceal", "aim", "charge"):
                lines.append(f"{actor} | {a.action_type}")
        round_narrative = "\n".join(lines)

        return {
            "finished": state.finished,
            "outcome": outcome,
            "player_hp": state.player_hp,
            "player_hp_max": state.player_hp_max,
            "player_san": state.player_san,
            "enemies": state.enemies,
            "round_log": state.log,
            "round_narrative": round_narrative,
            "is_boss": is_boss,
            "game_over": game_over,
            "round": state.round,
        }

